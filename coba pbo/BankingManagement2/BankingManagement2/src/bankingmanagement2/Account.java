    public void Bal(){
        String query = "INSERT INTO balances(Name, Acc, MICR_no, Balance) VALUES(?, ?, ?, ?)";
        try{
            java.sql.Connection conn = bankingmanagement2.connect.koneksiDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, name.getText());
            pst.setString(2, acc.getText());
            pst.setString(3, micr.getText());
            pst.setString(4, amount.getText());
            pst.execute();
        }catch (Exception e){
            e.printStackTrace();
        }
    }
    
