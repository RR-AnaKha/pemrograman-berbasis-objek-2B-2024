    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String query = "INSERT INTO account(Acc, Name, DOB, Pin, Acc_Type, Nationality, Caste, MICR_no, Gender, Mob, Address, SecQ, SecA, Balance) VALUES(?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
        try{
            java.sql.Connection conn = bankingmanagement2.connect.koneksiDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, acc.getText());
            pst.setString(2, name.getText());
            pst.setString(3, ((JTextField)dob.getDateEditor().getUiComponent()).getText());
            pst.setString(4, pin.getText());
            pst.setString(5, (String) acctype.getSelectedItem());
            pst.setString(6, (String) nation.getSelectedItem());
            pst.setString(7, caste.getText());
            pst.setString(8, micr.getText());
            
            jRadioButton1.setActionCommand("Male");
            jRadioButton2.setActionCommand("Female");
            pst.setString(9, buttonGroup1.getSelection().getActionCommand());
            pst.setString(10, mobile.getText());
            pst.setString(11, address.getText());
            pst.setString(12, secq.getText());
            pst.setString(13, answer.getText());
            pst.setString(14, amount.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Congratulations Your Account Has Been Created");
            Bal();
        }catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

